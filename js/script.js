navLinks.forEach((link, index) => {
    link.addEventListener('keydown', (e) => {
        if (e.key === 'ArrowRight' && index < navLinks.length - 1) {
            navLinks[index + 1].focus();
        }
        if (e.key === 'ArrowLeft' && index > 0) {
            navLinks[index - 1].focus();
        }
    });
});

// Focus trap for mobile menu
